<?php

declare(strict_types=1);

return [
    /*
    |--------------------------------------------------------------------------
    | 缓存键前缀
    |--------------------------------------------------------------------------
    | 缓存由 Laravel Illuminate Cache 驱动，此前缀用于区分 PToken 缓存条目
    */
    'cache_pre_key' => 'ptoken:',

    /*
    |--------------------------------------------------------------------------
    | Token 有效期（秒）
    |--------------------------------------------------------------------------
    | 默认 7 天 = 604800
    */
    'timeout' => 604800,

    /*
    |--------------------------------------------------------------------------
    | Token 分隔符
    |--------------------------------------------------------------------------
    | Token 格式：{version}{delimiter}{encryptedUserKey}{delimiter}{tokenId}
    | 使用 '.' 因为它不会出现在 Base64URL 编码中
    */
    'token_delimiter' => '.',

    /*
    |--------------------------------------------------------------------------
    | Token 格式版本号
    |--------------------------------------------------------------------------
    | 嵌入 token 字符串中，如 'v1'。未来格式变更时递增
    */
    'token_version' => 'v1',

    /*
    |--------------------------------------------------------------------------
    | AES-256-CBC 加密密钥
    |--------------------------------------------------------------------------
    | 必须为 32 字节。生产环境请通过环境变量注入
    */
    'encrypt_key' => env('PTOKEN_ENCRYPT_KEY', '12345678901234567890123456789012'),

    /*
    |--------------------------------------------------------------------------
    | 多端登录
    |--------------------------------------------------------------------------
    | true  = 同一用户允许多个 Token 同时有效
    | false = 仅最新生成的 Token 有效（旧 Token 自动失效）
    */
    'multi_login' => false,

    /*
    |--------------------------------------------------------------------------
    | 最大自动续期时间（秒）
    |--------------------------------------------------------------------------
    | 当 Token 剩余有效期不足此值时，自动续期并轮换 token。
    | 续期后新 token 会通过 new_token_header 响应头返回给前端。
    | 设为 0 可禁用自动续期。
    */
    'max_refresh' => 0,

    /*
    |--------------------------------------------------------------------------
    | Token 类型标识
    |--------------------------------------------------------------------------
    | 写入缓存 token_type 字段，用于区分不同类型的 token。
    | 示例：'access_token'、'api_key'、'personal_access_token'
    */
    'token_type' => 'access_token',

    /*
    |--------------------------------------------------------------------------
    | Token 签发者（iss）
    |--------------------------------------------------------------------------
    | 标识 token 由谁签发，留空不校验。
    | 示例：'https://api.example.com'
    */
    'issuer' => '',

    /*
    |--------------------------------------------------------------------------
    | Token 受众（aud）
    |--------------------------------------------------------------------------
    | 标识 token 的预期接收方，留空不校验。
    | 示例：'https://api.example.com'
    */
    'audience' => '',

    /*
    |--------------------------------------------------------------------------
    | 自定义请求头名称
    |--------------------------------------------------------------------------
    | 用于从请求中提取 token，默认 'Authorization'。
    | 可改为 'X-Api-Token' 等。
    */
    'token_header' => 'Authorization',

    /*
    |--------------------------------------------------------------------------
    | 响应头名称（自动续期后告知前端新 token）
    |--------------------------------------------------------------------------
    | 当 max_refresh 触发 token 轮换时，新 token 通过此响应头返回。
    | 默认 'X-New-Token'。
    */
    'new_token_header' => 'X-New-Token',

    /*
    |--------------------------------------------------------------------------
    | User Model 类名（FQCN）
    |--------------------------------------------------------------------------
    | 设置后中间件认证通过时将自动关联 User Model 实例。
    | 控制器中通过 $request->attributes->get('ptokenUser')->getUser() 获取。
    |
    | 示例：'App\Models\User' 或 \App\Models\User::class
    | null  = 不自动关联 User Model
    */
    'user_model' => null,

    /*
    |--------------------------------------------------------------------------
    | 认证排除路径
    |--------------------------------------------------------------------------
    | 中间件会跳过这些路径，不做认证检查
    */
    'auth_exclude_paths' => [
        '/api/login',
        '/api/register',
    ],
];
